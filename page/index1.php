        <h1>History</h1>
        <p style="color:green ;">The Usmanu Danfodiyo University, Sokoto (formerly University of Sokoto) is one of the four Universities established by the Federal Government of Nigeria in September 1975 , at which time three University Colleges (now full-fledged Universities) were established.
            The development of the university started on a temporary site (now called City Campus), 
            situated along Sultan Abubakar Road, Sokoto. classes started in October 20th, 1977, with an initial enrolment of ninety-three undergraduate students for the degrees of Bachelor of Arts, Bachelor of Arts/Science in Education and Bachelor of Science; and an academic staff strength of thirty-three.
            In January, 1978, one hundred and two students enrolled for a two-year pre-Degree Programme in the Humanities and Basics Sciences. With this modest start, the Usmanu Danfodiyo University, Sokoto formally graduated a total of seventy-two students during its first convocation held in November 22nd, 1980.
            Presently, there are Ten faculties and a postgraduate school in the University. 
            The faculties are: Agriculture, Arts and Islamic Studies, College of Health Sciences, Education and Extention Services, Law, Management Sciences, pharmaceutical Sciences and Veterinary Medicine. As the development programme is actively pursued, the following Faculties will be established: Food Sciences and Engineering.
            The University statute established the Center for Islamic Studies in 1982. 
            Its aim, among others, is to promote the study of and research in islam, Its instructions and related disciplines and its culture with special reference to the northern states of Nigeria. The Center runs a Diploma programme in Islamic Studies, which started in 1983, to assist in the manpower development of the locality and the country at large. the Center also runs Certificate Courses in Arabic and Islamic Studies.
            In November 1982, the Sokoto Energy Reserach Center was established at the instance of the Federal Government. 
            The Center was created not only to execute research in solar energy, but also to train and develop manpower in that area.
            The posgraduate school of the University was established in 1983 for the training of graduates in variuos disciplines at the Masters and Doctorate degree levels. At the convocation ceremony held in December , 1986, the school produced its first PhD graduate in Chemistry.
            The Universit
            y moved most of its faculties and Service Departments to the main campus in 1982. Phase 1 of Students’ hostels had already been completed and occupied.
            The City Campus now houses the Faculty of Veterinary Medcine, the college of Health Sciences, Center for Islamic studies, Cibiyar Nazarin Hausa, the department of Extention Services, a few services units and three Halls of Residence for students’ accomodation. The universioty Catering guest House, now named the University Guest Inn Limited, which runs on commercial basis, is located at Mabera Layout.
